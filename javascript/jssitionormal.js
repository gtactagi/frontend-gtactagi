function restaurarEstilos() {
  document.body.style.backgroundColor = 'white';
  const h2 = document.querySelector('h2');
  h2.style.backgroundColor = 'orange';
  h2.style.color = 'black';
  const section = document.querySelector('section');
  section.style.backgroundColor = 'purple';
  section.style.color='black';
  const divDescarga = document.querySelector('.descarga');
  divDescarga.style.backgroundColor = 'gold';
  divDescarga.style.color = '';
  const divcontainer = document.querySelector('.container');
  divcontainer.style.backgroundColor = 'silver';
  divcontainer.style.color = '';
}
